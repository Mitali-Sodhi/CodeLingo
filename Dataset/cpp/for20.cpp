#include  <iostream.h>

int main()
{
   int  number_to_print ;

   cout  <<  "\n Numbers from 0 to 20 are the following:\n\n " ;

   for ( number_to_print  =  0  ;
         number_to_print  <= 20 ;
         number_to_print  ++  )
   {
      cout  <<  " "  <<  number_to_print  ;
   }
}




