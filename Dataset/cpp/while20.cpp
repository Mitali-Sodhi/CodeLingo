#include  <iostream.h>

int main()
{
   int  number_to_print  =  0 ;

   cout << "\n Numbers from 0 to 20 are the following:\n\n " ;

   while ( number_to_print  <= 20 )
   {
      cout  <<  " "  <<  number_to_print  ;
      number_to_print  ++  ;
   }
}




