using System ;

class While20
{
   static void Main()
   {
      int  number_to_print  =  0 ;

      Console.Write( "\n Numbers from 0 to 20 are the following:\n\n ") ;

      while ( number_to_print  <= 20 )
      {
         Console.Write( " "  +  number_to_print ) ;
         number_to_print  ++  ;
      }
   }
}



