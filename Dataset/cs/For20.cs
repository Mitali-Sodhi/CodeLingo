using System ;

class For20
{
   static void Main()
   {
      int  number_to_print ;

      Console.Write( "\n Numbers from 0 to 20 are the following:\n\n ") ;

      for ( number_to_print  =  0  ;
            number_to_print  <= 20 ;
            number_to_print  ++  )
      {
         Console.Write( " "  +  number_to_print ) ;
      }
   }
}




